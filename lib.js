const fs = require("fs");
class GameDB {
	constructor() {
		this.characters = {};
		this.warehouse = {};
		this.settings = {};
		var buff = JSON.parse(fs.readFileSync("./Belongings.json", 'utf8'), "", 4);
		this.characters = buff.Characters;
		this.warehouse = buff.Items;
		this.settings = buff.Settings;
		this.resourceCount();


		// convert all WareHouse Jasper into an actual xp
		for (var key in this.warehouse.jaspers) {
			var buff = this.warehouse.jaspers[key]
			this.warehouse.jaspers[key] = ((3000*buff["purple"])+(750*buff["blue"])+(150*buff["green"]));
		}
	}
	// counts how much xp need to upgrade lvl and equipment
	resourceCount() {
		for (var key in this.characters) {
			if (this.characters[key].ignore) {
				delete this.characters[key];
				continue;
			}
			if (this.characters[key].stars > 3) {
				this.characters[key].EXstars = 6;
			} else {
				this.characters[key].EXstars = 4;
			}
			this.characters[key].EXlevel = this.convertLevel(this.characters[key].level);
			this.characters[key].EXtargetLvl = this.convertLevel(this.characters[key].targetLvl);

			this.characters[key].EXxp = 0;
			this.characters[key].EXmoney = 0;
			for (var i=this.characters[key].EXlevel-1;i<this.characters[key].EXtargetLvl-1;i++) {
				this.characters[key].EXxp += Table["lvls"][this.characters[key].stars][i][0];
				this.characters[key].EXmoney += Table["lvls"][this.characters[key].stars][i][1];
			}
			this.characters[key].EXsublime = 0;
			for (var i=this.characters[key].equipment-1;i<this.characters[key].targetEquipment-1;i++) {
				this.characters[key].EXsublime += Table["equipment"][this.characters[key].EXstars][i][0];
				this.characters[key].EXmoney += Table["equipment"][this.characters[key].EXstars][i][1]
			}
		}
	}
	convertLevel(input) {
		var buff = parseInt(input.slice(3));
		switch (input[1]) {
			case "0": 
				return buff;
				break;
			case "1":
				return buff+30;
				break;
			case "2":
				return buff+70;
				break;
			case "3":
				return buff+120;
				break;
			default:
				throw new Error("Error in \"Belongings\" file");
				break;
		}
	}
	totalMoney() {
		var count = 0;
		for (var key in this.characters) {
			count += this.characters[key].EXmoney;
		}
		return count;
	}
	totalSublime() {
		var count = 0;
		for (var key in this.characters) {
			count += this.characters[key].EXsublime;
		}
		return count;
	}
	totalXP() {
		var buff = {
			"Fire": 0,
			"Water": 0,
			"Forest": 0,
			"Thunder": 0
		};
		for (var key in this.characters) {
			buff[this.characters[key].element] += this.characters[key].EXxp;
		}
		return buff;
	}
	countPrism() {
		// 300 subline = 7 blue and 10 green = 55000 + bonus = 30 prism
		var buff = 0;
		var buff2 = 0;
		var buff3 = 0;
		for (var key in this.characters) {
			buff += this.characters[key].EXxp;
			buff2 += this.characters[key].EXsublime;
			buff3 += this.characters[key].EXmoney;
		}
		buff /= (750*7+150*10); // how much runs we need to do for xp
		buff2 /= 300; // weapon
		buff3 /= (55000+(this.warehouse.average_bonus_nightium/3)); //money
		buff = (buff + buff2 + buff3) * 30; // 1 run = 30 prism
		return buff;
	}

	// can't make better solution atm, so will use dumb one
	countPrism2(input) {
		var runs = 0;
		runs = input[0]/(55000+(this.warehouse.average_bonus_nightium/3));
		runs += input[1]/300;
		runs += input[2]/(750*7+150*10);
		runs *= 30;
		return runs;
	}

	// stats for how much resources were wasted
	alredySpentResources() {
		for (var key in this.characters) {
			this.characters[key].targetLvl = this.characters[key].level;
			this.characters[key].level = "A0 1";
			this.characters[key].targetEquipment = this.characters[key].equipment;
			this.characters[key].equipment = 1;
		}
		this.resourceCount();

		console.log(this.characters)
	}
}


